/** 全書公式速查（由各章公式彙整）。 */
export function allFormulas(): { book: string; color: string; chapter: string; formula: Formula }[] {
  const out: { book: string; color: string; chapter: string; formula: Formula }[] = [];
  for (const b of TEXTBOOKS) {
    for (const u of b.units) {
      for (const c of u.chapters) {
        for (const f of c.formulas ?? []) {
          out.push({ book: b.title, color: b.color, chapter: `${c.ch} ${c.title}`, formula: f });
        }
      }
    }
  }
  return out;
}
